          </label>
        </el-col>
        <el-col>
          <label>
          （六）阅读本说明后，仍然无法准确填报的，请及时向海关有关工作人员询问。
          </label>
        </el-col>
        <el-col><label>二、具体栏目填报说明：</label>
        </el-col>
        <el-col><label>（一）“补充申报单编号”由海关填写。</label>
        </el-col>
        <el-col>
          <label>
            （二）“报关单号”应填写补充申报单所对应的报关单编号。
          </label>
        </el-col>
        <el-col>
          <label>
            （三）“报关单 第  项”是指作补充申报的商品在报关单中的排列序号，该项目与报关单中该商品的“项号”一致。
          </label>
        </el-col>
        <el-col>
          <label>
            （四）“商品名称”为补充申报的进口货物规范的中英文商品名称，内容应当与报关单相应项目相符。
          </label>
        </el-col>
        <el-col>
          <label>
           （五）“商品编码”应填报由《中华人民共和国进出口税则》确定的进出口货物的税则号列和《中华人民共和国海关统计商品目录》确定的商品编码，以及符合海关监管要求的附加编号组成的10位商品编码，该项目应与报关单填报内容一致。
          </label>
        </el-col>
        <el-col>
          <label>
          （六）“规格型号”内容应当与报关单相应栏目相符，但应当比报关单所填报的规格型号内容更详细全面。
          </label>
        </el-col>
        <el-col>
          <label>
          （七）“品牌”栏根据实际情况，有中英文的则填写中英文，仅有一种的则填一种。以上“规格型号”、“品牌”栏要求填写的项目较多，如果填写空间不够，应当选择能够反映进出口货物特征的主要项目填写。对部分重点商品海关将以清单形式明确商品规格型号及品牌的填制规范。
          </label>
        </el-col>
        <el-col>
          <label>
          （八）“买方”是指直接与境外的卖方谈判达成交易，自行或者委托具有对外贸易经营权的企业与卖方签订并实际执行合同的我国境内法人或其他组织。买方也就是成交单位，同时也可以是收发货人。该栏目必须填写中英文全名，主要办公地点地址、联系人姓名以及联系电话。
          </label>
        </el-col>
        <el-col>
          <label>
          （九）“卖方”指与买方谈判达成交易，并直接与成交单位或与成交单位委托的收发货人签订进口货物销售合同的境外法人或其他组织。卖方就是成交外商，也可以是生产厂商。该栏目必须填写中英文全名，主要办公地点地址、联系人姓名以及联系电话。
          </label>
        </el-col>
        <el-col>
          <label>
          （十）“生产厂商”指进口货物的生产企业，该栏目必须填写其中英文全名、主要办公地点地址、联系人姓名以及联系电话。
          </label>
        </el-col>
        <el-col>
          <label>
          （十一）“合同协议号”应与报关单填报内容一致。
          </label>
        </el-col>
        <el-col>
          <label>
          （十二）“发票编号”填写生产厂商销售货物开出的商业发票编号。如果涉及反倾销商品，本栏目必须填写原产商发票。
          </label>
        </el-col>
        <el-col>
          <label>
          （十三）“商品其他名称”填写除《报关单》中商品名称以外的其他名称：如俗名、习惯称谓、商业名称、外文名称等。
          </label>
        </el-col>
        <el-col>
          <label>
          （十四）“进/出口国（地区）海关商品编码” 填写进出口该商品的国家或地区海关确定的商品编码，如果不清楚可以不填。
          </label>
        </el-col>
        <el-col>
          <label>
          （十五）“该商品是否取得过海关预归类决定书”栏，应根据实际选择，如果该商品曾取得过海关的预归类决定书，还应填写“预归类决定书编号”“商品编码”“作出预归类决定的海关”相关内容。
          </label>
        </el-col>
        <el-col>
          <label>
          （十六）“该商品是否曾被海关取样化验”栏，请根据实际选择。
          </label>
        </el-col>
        <el-col>
          <label>
          （十七）“请按选中项填写相关说明”栏，请根据海关要求或企业希望主动提供资料的实际情况进行选择，并在空白栏处按选中项目的字母顺序逐项如实填写，也可在申报单后随附提供。
          </label>
        </el-col>
